package com.iscas.sdas.dto.cell;

public class CellDto {

	private String e_utrancell;
	private String network_name;
	private String used_band;
	private String cover_scene;
	private String station_name;
	private String group_type;
	
	
	
	public String getE_utrancell() {
		return e_utrancell;
	}
	public void setE_utrancell(String e_utrancell) {
		this.e_utrancell = e_utrancell;
	}
	public String getNetwork_name() {
		return network_name;
	}
	public void setNetwork_name(String network_name) {
		this.network_name = network_name;
	}
	public String getUsed_band() {
		return used_band;
	}
	public void setUsed_band(String used_band) {
		this.used_band = used_band;
	}
	public String getCover_scene() {
		return cover_scene;
	}
	public void setCover_scene(String cover_scene) {
		this.cover_scene = cover_scene;
	}
	public String getStation_name() {
		return station_name;
	}
	public void setStation_name(String station_name) {
		this.station_name = station_name;
	}
	public String getGroup_type() {
		return group_type;
	}
	public void setGroup_type(String group_type) {
		this.group_type = group_type;
	}
}
